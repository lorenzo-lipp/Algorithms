let { mult } = require("./long-multiplication");

describe("Testing mult", () => {
    it("should be a function", () => {
        expect(typeof mult).toBe("function");
    });

    it("should return a string", () => {
        expect(typeof mult("18446744073709551616", "18446744073709551616")).toBe("string");
    });

    it("should return 340282366920938463463374607431768211456 as the result of 18446744073709551616 x 18446744073709551616", () => {
        expect(mult("18446744073709551616", "18446744073709551616")).toBe("340282366920938463463374607431768211456");
    });

    it("should return 58743055272886011737990786529368211456 as the result of 1844674407309551616 x 31844674073709551616", () => {
        expect(mult("1844674407309551616", "31844674073709551616")).toBe("58743055272886011737990786529368211456");
    });

    it("should return 82816580680737279241781007431768211456 as the result of 1846744073709551616 x 44844644073709551616", () => {
        expect(mult("1846744073709551616", "44844644073709551616")).toBe("82816580680737279241781007431768211456");
    });

    it("should return 3402823669833978308014392742590611456 as the result of 1844674407709551616 x 1844674407370951616", () => {
        expect(mult("1844674407709551616", "1844674407370951616")).toBe("3402823669833978308014392742590611456");
    });

    it("should return 5247498076580334548376218009219475456 as the result of 2844674407370951616 x 1844674407370955616", () => {
        expect(mult("2844674407370951616", "1844674407370955616")).toBe("5247498076580334548376218009219475456");
    });
});